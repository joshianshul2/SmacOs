class Paterninc
{
    public static void main(String[] args)
    {
        int j,i,k=1;
        for(i=1;i<=5;i++)
        {
            for(j=1;j<=i;j++)
            {
                System.out.print(k);
                k++;
            }
            System.out.println();
        }
        
    }
}
